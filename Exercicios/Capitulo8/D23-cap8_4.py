"""
(30/04/2021) Escreva uma função que receba a base e a altura de um triângulo e retorne sua área (A = (base x altura) / 2)
Valores esperados:
area_triangulo(6, 9) == 27
"""

def area_triangulo(base, altura):
    return (base * altura) / 2


print('area_triangulo(6, 9) =>', area_triangulo(6, 9))
print('area_triangulo(5, 8) =>', area_triangulo(5, 8))
